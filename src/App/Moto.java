package App;

public class Moto extends Veiculo {
	
	private String tipoFreio;
	private int quantCilindrada;
	private int peso;
	
	



	public String getTipoFreio() {
		return tipoFreio;
	}

	public void setTipoFreio(String tipoFreio) {
		this.tipoFreio = tipoFreio;
	}

	public int getQuantCilindrada() {
		return quantCilindrada;
	}



	public void setQuantCilindrada(int quantCilindrada) {
		this.quantCilindrada = quantCilindrada;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

}
